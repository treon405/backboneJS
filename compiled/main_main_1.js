function autocollapse() {
    var navbar = $('.navbar.navbar-collapse');
    navbar.removeClass('collapsed');
    if(navbar.innerHeight() > 50) {
    	navbar.addClass('collapsed');
    }
}
$(document).on('ready', autocollapse);
$(window).on('resize', autocollapse);
